package Array;

public class swapping {

	public static void main(String[] args)
	{
		
		int a=20;
		int b=50;
		
		int temp;
		
		temp=a;
		a=b;
		b=temp;
		
		System.out.println("The value of A--->"+a);
		System.out.println("The value of B--->"+b);
		
		
		int c=1000;
		int d=500;
		
		c=c+d;
		d=c-d;
		c=c-d;
		

		System.out.println("The value of c--->"+c);
		System.out.println("The value of D--->"+d);
	}
}
